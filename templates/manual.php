          <p>3.4. Futulabi kasutamisel on keelatud igasugune tegevus, mis võib kahjustada Futulabi toimimist või on vastuolus õigusaktidega. Keelatud tegevuse tuvastamisel on Tartu Ülikoolil õigus piirata kasutaja õigusi Futulabis või kustutada tema lisatud isikuandmed. </p>
          <p>3.5 Keelatud tegevuse all peetakse silmas muu hulgas Futulabi ülekoormamist päringutega, ebaturvalise või -seadusliku sisu lisamist süsteemi, autoriõiguste rikkumist või muud etteheidetavat käitumist või sellisele käitumisele kaasaaitamist.</p>
