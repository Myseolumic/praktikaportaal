        <div data-aos="fade-down">
          <p>2.2. Projektitaotluse esitamiseks peab kasutaja täitma projektivormi. Selleks tuleb see Wordi failina Futulabi lehelt alla laadida ja ära täita. Futulabi veebilehel tuleb projekti esitamise aknasse kirjutada projekti nimetus, oma ees- ja perekonnanimi, e-posti aadress, organisatsiooni nimi ja meeskonna suurus. Seejärel tuleb täidetud projektivorm PDF-vormingus üles laadida. </p>

          <p>Kõik kasutaja sisestatud isikuandmed avalikustatakse Futulabis projektide alamlehel (<a href="https://praktika.ut.ee/projektid" target="_blank">https://praktika.ut.ee/projektid</a>). Esitatud andmed on vajalikud selleks, et üliõpilased saaksid projektiga liituda ning osaleda õppetöös ehk projektipraktikas. Projekti lõpufaasis registreerivad õppekorraldusspetsialistid üliõpilased ÕIS-is.</p>
        </div>
        <div data-aos="fade-down">

          <p>2.3. Praktikapakkumise lisamiseks on kasutajal kaks võimalust: ta võib täita praktikapakkumiste alamlehel (<a href="https://praktika.ut.ee/praktikapakkumised" target="_blank">https://praktika.ut.ee/praktikapakkumised</a>) praktikapakkumise vormi või laadida pakkumise üles PDF-vormingus ja esitada lisaandmed.</p>

          <p>Praktikapakkumise lisanud kasutaja andmeid säilitatakse kuni praktikapakkumises nimetatud registreerimistähtaja lõpuni. Soovi korral on kasutajal õigus esitada igal hetkel e-posti aadressile <a href="mailto:praktika.ut.ee">praktika@ut.ee</a> taotlus oma praktikapakkumise kustutamiseks. Taotluse alusel kustutatakse praktikapakkumise andmed jäädavalt.</p>
        </div>
