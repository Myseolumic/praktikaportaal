          <h5 class="text-uppercase font-weight-bold mb-3 mt-5">2. Futulabi kasutamine</h5>
          <p>Futulabi kasutaja saab:</p>
          <ul>
            <li>lisada profiili, tutvuda teiste Futulabi lisatud profiilidega ja võtta huvi korral ühendust profiili lisanud isikuga;</li>
            <li>esitada projektitaotlust, tutvuda teiste Futulabi lisatud projektidega ja võtta huvi korral ühendust projekti lisanud isikuga;</li>
            <li>lisada praktikapakkumise, tutvuda teiste Futulabi lisatud praktikapakkumistega ja võtta huvi korral ühendust praktikapakkujaga;</li>
            <li>liituda projektiga.</li>
          </ul>
        </div>

        <div data-aos="fade-down">
          <p>2.1. Profiili lisamine on vabatahtlik. Üliõpilane saab praktikapakkumisi vaadata ka ilma profiili lisamata. Profiili lisamine annab üliõpilasele võimaluse olla tööandjatele rohkem nähtav ja ennast turundada. Isikuandmed, mida kasutaja veebilehele sisestab, avalikustatakse Futulabis (<a href="https://praktika.ut.ee/uliopilased" target="_blank">https://praktika.ut.ee/uliopilased</a> ja/või <a href="https://praktika.ut.ee/projektipraktika/" target="_blank">https://praktika.ut.ee/projektipraktika</a>).</p>

          <ul>
            <li>Profiili lisamiseks Futulabi on kasutajal kohustus sisestada oma ees- ja perekonnanimi, e-posti aadress, andmed eriala, instituudi, soovitava praktika-/töövaldkonna, tugevate külgede ja oskuste kohta. </li>
            <li>Ees- ja perekonnanimi tuleb avalikustada, et tööandja teaks, kelle profiili ta vaatab, kui ta otsib endale praktikanti. </li>
            <li>E-posti aadress tuleb avalikustada, et tööandja saaks talle sobiva kandidaadiga ühendust võtta. </li>
            <li>Andmed eriala ja instituudi kohta tuleb avalikustada, et tööandja saaks aru, milliseid õpiteadmisi tulevane praktikant hetkel omandab, ja oskaks selle põhjal otsida enda ettevõttesse/organisatsiooni sobivat isikut.</li>
            <li>Andmed soovitava praktika-/töövaldkonna kohta tuleb avalikustada, et tööandja saaks teada, millistest pakkumistest üliõpilane on huvitatud. </li>
            <li>Andmed tugevate külgede ja oskuste kohta tuleb avalikustada, et tööandja saaks parema ülevaate, kas selliste omadustega kandidaat tema jaoks sobib. </li>

          </ul>
