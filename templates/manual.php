          <h5 class="text-uppercase font-weight-bold mb-3 mt-5">3. Kasutaja õigused, kohustused ja vastutus</h5>
          <p>3.1. Kasutaja võib soovi korral sisestada ja avalikustada Futulabi lehel oma isikuandmed, et olla tööandja jaoks nähtavam. </p>
          <p>3.2. Kasutajal on võimalus projektiga liituda ainult siis, kui ta avalikustab oma ees- ja perekonnanime ja eriala Futulabis.</p>
