        <div data-aos="fade-down">
          <h5 class="text-uppercase font-weight-bold mb-3 mt-5">1. Üliõpilane</h5>
          <ul>
            <li>Üliõpilane saab lisada oma profiili. Tööandjal on võimalik sellega tutvuda ja leida enda ettevõttesse motiveeritud praktikante.</li>
            <li>Profiili kuvatakse keskkonnas kuus kuud, pärast seda see kustub ja üliõpilane võib oma profiili uuesti lisada.</li>
          </ul>
        </div>

        <div data-aos="fade-down">
          <h5 class="text-uppercase font-weight-bold mb-3 mt-5">2. Projektipraktika</h5>
          <p>Kasutajale teenuse osutamiseks haldab Futulab veebilehe kaudu alljärgnevaid kasutaja avalikustatud isikuandmeid: </p>
          <ul>

            <li>Projektipraktika on üleülikooliline õppeaine (SVMJ.02.014), mida saavad valida kõik üliõpilased valik- või vabaainena. Tutvu <a href="https://www.is.ut.ee/rwservlet?oa_aine_info.rdf+1231563+PDF+64671946656914327937+application/pdf" target="_blank">ainekaartiga</a>.</li>
            <li>Üliõpilane saab projektipraktika läbimise eest 6 EAP-d.</li>
            <li>Ülikooli õppejõud saab projektipraktika juhendamise eest 4 EAP-d oma koormusearvestusele lisaks. Juhendamine pakub õppejõule teadmisi, kuidas juhtida valdkondadevahelisi protsesse, panna üliõpilasi tegema valdkondadevahelist koostööd ja arendada nende ettevõtlikkust.</li>
            <li>Projektipraktikat koordineerib majandusteaduskond. Lisateavet saab aadressil <a href="mailto:praktika@ut.ee">praktika@ut.ee</a>.</li>
          </ul>
        </div>

        <div data-aos="fade-down">
          <h5 class="text-uppercase font-weight-bold mb-3 mt-5">2.1. Projektipraktika idee esitamine</h5>
          <ul>
            <li>Üliõpilastelt, õppejõududelt, ülikooli töötajatelt ja tööandjatelt oodatakse projektipraktika ideid.</li>
            <li>Idee esitamiseks tuleb alla laadida Wordi failis projektivorm. Selleks tuleb minna lehele <a target="_blank" href="https://praktika.ut.ee/projektipraktika">praktika.ut.ee/projektipraktika</a>, klõpsata väljal „Esita projekt“ ja seejärel üleval vasakul valikul „Projektivorm“.</li>
            <li>Täidetud projektivormi esitamiseks tuleb minna lehele <a target="_blank" href="https://praktika.ut.ee/projektipraktika">praktika.ut.ee/projektipraktika</a>, klõpsata väljal „Esita projekt“, seejärel üleval vasakul valikul „Esita projekt“ ja laadida täidetud projektivorm PDF-vormingus üles. </li>
          </ul>
        </div>

        <div data-aos="fade-down">
          <h5 class="text-uppercase font-weight-bold mb-3 mt-5">2.2. Projektipraktikas osalemine</h5>
          <ul>
            <li>Lehel <a target="_blank" href="https://praktika.ut.ee/projektipraktika">praktika.ut.ee/projektipraktika</a> saab valida endale huvipakkuva projekti ja sellega liituda.</li>
            <li>Sügissemestril liitutakse projektiga oktoobrikuu alguses, kevadsemestril märtsikuu alguses.</li>
            <li>Projektiga liitumise järel on võimalik registreeruda projektipraktika kursusele Moodle’is, kus on kirjas kursuse korraldus ja toimub kohustuslike tööde esitamine.</li>
            <li>ÕIS-is registreeritakse üliõpilane ainele kursuse lõpus.</li>
          </ul>
