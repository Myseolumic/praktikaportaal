        <div data-aos="fade-down">
          <h5 class="text-uppercase font-weight-bold my-3">Futulabi veebilehe andmekaitsetingimused</h5>
          <p>Siinsetes andmekaitsetingimustes antakse kasulikku infot selle kohta, kuidas ja milleks Futulabi keskkond isikuandmeid töötleb. Andmekaitsetingimuste koostamisel on lähtutud isikuandmete kaitse üldmäärusest <a href="https://eur-lex.europa.eu/legal-content/ET/TXT/HTML/?uri=CELEX:32016R0679&from=ET" target="_blank">(EL) 2016/679</a>.</p>
          <p>Andmekaitsetingimustega reguleeritakse isikuandmete töötlemist ja küpsiste kasutamist <a target="_blank" href="https://praktika.ut.ee">Tartu Ülikooli Futulabi</a> (edaspidi Futulab) veebilehel <a target="_blank" href="https://praktika.ut.ee">https://praktika.ut.ee</a>. Futulab on vabatahtlik praktikavahenduskeskkond. Isikuandmed, mida kasutaja veebilehele sisestab, avalikustatakse Futulabis (<a target="_blank" href="https://praktika.ut.ee/uliopilased">https://praktika.ut.ee/uliopilased</a> ja/või <a target="_blank" href="https://praktika.ut.ee/projektipraktika">https://praktika.ut.ee/projektipraktika</a>).</p>
          <p>Futulabi oma profiili lisades, projektipraktika ideed esitades ja projektiga liitudes annab kasutaja õiguse andmete töötlemiseks kooskõlas siinsete andmekaitsetingimustega.
            Andmekaitsetingimustes ei käsitleta seda, kuidas töötleb Futulab juriidiliste isikute andmeid, ega seda, kuidas teised isikud isikuandmeid töötlevad. Samuti ei hõlma need isikuandmete töötlemist võõrastel veebilehtedel, millele viidatakse praktika.ut.ee veebilehtedel välislinkidega.</p>
        </div>
        <div data-aos="fade-down">
          <h5 class="text-uppercase font-weight-bold mb-3 mt-5">1. Mõisted</h5>
          <ul>
            <li>Isikuandmete töötlemine on igasugune veebilehe kasutaja isikuandmetega tehtav tegevus – andmete kogumine, talletamine, parandamine või kustutamine.</li>
            <li>Kasutaja on füüsiline ja juriidiline isik (nt üliõpilane, tööandja, õppejõud), kes külastab veebilehte ja kasutab Futulabi teenuseid. </li>
            <li>Teenused on kõik Futulabi pakutavad teenused, sh praktikapakkumise lisamine, projektipraktika idee esitamine, projektiga liitumine, profiili lisamine, seminaride tutvustamine, projekti taotlusvormi allalaadimine jne. </li>
            <li>Kolmas isik on Futulabi lepingupartner Google Analytics, <a href="https://policies.google.com/technologies/partner-sites" target="_blank">kes töötleb isikuandmeid</a> ja kelle teenuse - veebilehe kasutusstatistika analüüsi - abil pakub Futulab kasutajale teenuseid.</li>
          </ul>
        </div>

        <div data-aos="fade-down">
          <h5 class="text-uppercase font-weight-bold mb-3 mt-5">2. Isikuandmete töötlemine</h5>
          <p>Kasutajale teenuse osutamiseks haldab Futulab veebilehe kaudu alljärgnevaid kasutaja avalikustatud isikuandmeid: </p>
          <ul>
            <li>profiili lisamisel ja projektimeeskonnaga liitumisel: kasutaja nimi ja e-posti aadress, andmed eriala ja instituudi kohta, tema tugevad küljed;</li>
            <li>praktikapakkumise lisamisel: kasutaja nimi ja e-posti aadress, telefoninumber, organisatsiooni nimi;</li>
            <li>projektitaotluse esitamisel: kasutaja nimi ja e-posti aadress, organisatsiooni nimi;</li>
            <li>veebilehe külastatavuse statistika tegemisel isikustamata Google Analyticsi abil.</li>
          </ul>
          <p>Lisaks kasutab Futulab kolmanda isiku teenuseid, et analüüsida veebilehe kasutusstatistikat. Futulab ja Futulabis avalikustatud isikuandmed on Google’i otsingumootorile suletud.</p>
        </div>

        <div data-aos="fade-down">
          <h5 class="text-uppercase font-weight-bold mb-3 mt-5">3. Lingid teistele lehtedele ja manustatud sisu</h5>
          <p>Osal lehtedel, näiteks avalehel, on viited teistele kodulehtedele või need sisaldavad manustatud sisu, näiteks teavet praktikaprotsessi kohta, millega saab tutvuda ka sisu.ut leheküljel. Manustatud sisu tarbimisel külastab kasutaja tehnilises mõttes teist kodulehte, kus kehtivad selle kodulehe tingimused.</p>
          <p>Need lehed võivad asendada küpsiseid, koguda kasutaja andmeid ja edastada neid kolmandatele isikutele.</p>
        </div>

        <div data-aos="fade-down">
          <h5 class="text-uppercase font-weight-bold mb-3 mt-5">4. Isikuandmete säilitamine ja turvalisus</h5>
          <p>Futulab säilitab profiilides sisalduvaid isikuandmeid kuni kuus kuud või senikaua, kuni kasutaja esitab taotluse oma andmete kustutamiseks. Praktikapakkumisi kuvatakse veebilehel kuni pakkumise lõpptähtajani. Esitatud projekte kuvatakse veebilehel jooksvas järjekorras ning projektide lõppemisel isikuandmeid kustutatakse ja projektid arhiveeritakse. Arhiveeritud projektides jäävad Futulabis avalikuks projekti nimi, eesmärk, tegevus ja tulemus. </p>
        </div>

        <div data-aos="fade-down">
          <h5 class="text-uppercase font-weight-bold mb-3 mt-5">5. Kasutaja õigused</h5>
          <p>Kasutajal on õigus nõuda igal ajal isikuandmete:</p>
          <ul>
            <li>parandamist, täiendamist või kustutamist;</li>
            <li>töötlemise piiramist.</li>
          </ul>
          <p>Oma õiguste kasutamiseks tuleb kasutajal saata e-posti aadressile praktika@ut.ee vastavasisuline sooviavaldus.</p>
          <p>Kui kasutaja usub, et tema õigusi on isikuandmete töötlemisel rikutud, on tal õigus pöörduda Andmekaitse Inspektsiooni poole.</p>
        </div>

        <div data-aos="fade-down">
          <h5 class="text-uppercase font-weight-bold mb-3 mt-5">6. Küpsised</h5>
          <p>Futulabi veebilehel kasutatakse parema kasutajakogemuse tagamiseks kahte tüüpi küpsiseid:</p>
          <ul>
            <li>püsivad küpsised, mis jäävad kasutaja arvutis olevasse küpsiste faili. Neid võidakse kasutada näiteks kasutaja kui veebilehe korduvkülastaja äratundmiseks ja veebilehe sisu kohandamiseks vastavalt kasutaja vajadustele või statistiliste andmete kogumiseks;</li>
            <li>seansiküpsised, mis on ajutised ja kaovad, kui kasutaja veebilehelt lahkub või sulgeb oma veebilehitseja. Seansiküpsiseid võidakse kasutada veebilehe teatud funktsioonide, näiteks teenuse või toote taotlemise jms võimaldamiseks.</li>
          </ul>
          <p>Küpsiseid kasutatakse teabe kogumiseks selle kohta, kuidas veebilehte kasutatakse, et muuta seda kasutajatele isiklikumaks ja mugavamaks. Lisaks aitavad küpsised koguda kasutajastatistikat, mis võimaldab mõõta ja parandada veebilehe toimivust ning annab alusandmed tõhusaks turundustegevuseks.</p>
          <p>Kasutaja loetakse küpsistega nõustunuks, kui tema veebilehitseja seadistustes on küpsised lubatud. Veebilehitsejad lubavad tihtipeale vaikimisi küpsisefailide säilitamist kasutaja seadmesse. Kasutaja võib oma seadmes küpsiste kasutamise ära keelata või need kustutada, muutes oma veebilehitseja seadeid. Kui küpsiseid ei lubata, ei pruugi veebileht korrektselt töötada.</p>
          <p>Google Analytics võib koguda andmeid kasutajate aktiivsuse kohta ka ilma küpsisefaile kasutamata. Et Google Analytics ei saaks andmeid kasutada, tuleb kasutajal paigaldada oma seadmesse Google Analyticsit blokeeriv rakendus, milleks on Google Analyticsist loobumise brauserilaiend.</p>
