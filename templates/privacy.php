        <h5 class="text-uppercase font-weight-bold mb-3 mt-5">Küpsised</h5>
        <p>Futulabi veebilehel kasutatakse küpsiseid, eesmärgiga tagada parem kasutajakogemus.</p>
        <p>Küpsis on väike tekstifail, mille veebilehitseja automaatselt kliendi poolt kasutatavasse seadmesse salvestab. Küpsiseid kasutatakse teabe kogumiseks selle kohta, kuidas klient veebilehte kasutab, et kliendi jaoks veebilehe kasutamist isiklikumaks ja kasutajasõbralikumaks muuta. Küpsised võivad olla loodud erinevate teenusepakkujate poolt, näiteks Google ja Facebook. Küpsisefailid ei ole arvuti jaoks ohtlikud.</p>
        <p>Küpsiseid on kahte tüüpi:</p>
        <ol>
          <li style="list-style-type: decimal;">
            <p>Püsivaid küpsiseid, mis jäävad kasutaja arvutis olevasse küpsiste faili püsivalt. Neid võidakse kasutada näiteks kasutaja kui veebilehe korduvkülastaja äratundmiseks ja veebilehe sisu kohandamiseks vastavalt kasutaja vajadustele või statistiliste andmete kogumiseks.</p>
          </li>
          <li style="list-style-type: decimal;">
            <p>Seansiküpsised on ajutised ja kaovad, kui kasutaja veebilehelt lahkub või oma veebilehitseja sulgeb. Seansiküpsiseid võidakse kasutada veebilehe teatud funktsioonide, näiteks teenuse või toote taotlemise jms võimaldamiseks.</p>
          </li>
        </ol>
        <p>Küpsiseid kasutatakse teabe kogumiseks selle kohta, kuidas veebilehte kasutatakse, et seda kasutajatele isiklikumaks ja kasutajasõbralikumaks muuta. Lisaks aitavad küpsised koguda kasutajastatistikat, mis võimaldab mõõta ja parandada veebilehe toimivust ning annab alusandmed, et viia läbi efektiivne turundustegevus.</p>
        <p>Kasutaja loetakse küpsistega nõustunuks, kui tema veebilehitseja seadistustes on küpsised lubatud. Veebilehitsejad lubavad tihtipeale vaikimisi küpsisefailide säilitamist tarbija seadmesse. Kasutaja võib oma seadmes küpsiste kasutamise ära keelata või need kustutada, muutes oma veebilehitseja vastavaid seadeid. Küpsiste mittekasutamisel ei pruugi veebileht korrektselt töötada.</p>
        <p>Kasutusstatistika kogumiseks kasutab Futulab Google Analytics teenust, kogudes andmeid külastatud alalehtede, külastusaja ja muu statistika kohta.</p>
        <p>Google Analytics võib siiski koguda andmeid kasutajate aktiivsuse kohta ka ilma küpsisefaile kasutamata. Selleks, et Google Analytics ei saaks andmeid kasutada, paigalda oma seadmesse Google Analytics'i blokeeriv rakendus: <a target="_blank" href="https://tools.google.com/dlpage/gaoptout?hl=en">Google Analyticsist loobumise brauserilaiend</a>.</p>
